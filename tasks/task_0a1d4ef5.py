"""Generator."""

import common


def generate(width=None, height=None, wides=None, talls=None, brows=None,
             bcols=None, colors=None, bgcolors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    colors: A list of colors to use.
  """

  if width is None:
    width, height = common.randint(3, 3), common.randint(2, 4)
    # Determine the invisible grid.
    lengths = [8, 8, 8]
    while True:
      depths = [common.randint(6, 10) for _ in range(height)]
      if sum(depths) <= 28: break
    # Determine the dimensions and placement of the boxes
    wides, talls, brows, bcols = [], [], [], []
    for row in range(height):
      for col in range(width):
        length, depth = lengths[col], depths[row]
        wides.append(common.randint(3, length - 1))
        talls.append(common.randint(3, depth - 1))
        lengthsum, depthsum = sum(lengths[:col]), sum(depths[:row])
        brows.append(1 + depthsum + common.randint(0, depth - 1 - talls[-1]))
        bcols.append(3 + lengthsum + common.randint(0, length - 1 - wides[-1]))
    # Determine the background colors
    background = common.random_colors(2)
    # Determine the box colors (no two adjacent should be the same)
    while True:
      colors = [common.random_color(exclude=background) for _ in range(width * height)]
      good = True
      for row in range(height):
        for col in range(width):
          if row and colors[row * width + col] == colors[(row - 1) * width + col]:
            good = False
          if col and colors[row * width + col] == colors[row * width + col - 1]:
            good = False
      if good: break
    # Determine the background (make half of them black, no squares allowed).
    while True:
      bgcolors = common.choices(background, 30 * 30)
      bgcolors = [color if common.randint(0, 1) else 0 for color in bgcolors]
      good = True
      for row in range(1, height - 1):
        for col in range(1, width - 1):
          subset = []
          for dr in [-1, 0, 1]:
            for dc in [-1, 0, 1]:
              subset.append(bgcolors[(row + dr) * width + col + dc])
          if 0 not in subset and len(set(subset)) == 1: good = False
      if good: break

  grid, output = common.grid(30, 30), common.grid(width, height)
  for i, color in enumerate(bgcolors):
    grid[i // 30][i % 30] = color
  for row in range(height):
    for col in range(width):
      i = row * width + col
      output[row][col] = colors[i]
      wide, tall, brow, bcol = wides[i], talls[i], brows[i], bcols[i]
      common.rect(grid, wide, tall, brow, bcol, colors[i])
  return {"input": grid, "output": output}


