"""Generator."""

import common


def generate(width=None, height=None, answer=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    width: The width of the grid.
    height: The height of the grid.
    answer: The answer to the puzzle.
    colors: A list of colors to use.
  """

  if width is None:
    base, bgcolor = common.randint(21, 28), common.random_color(exclude=[4])
    width, height = base + common.randint(-1, 1), base + common.randint(-1, 1)
    grid = common.grid(width, height)
    for row in range(height):
      if common.randint(0, 1): continue
      for col in range(width):
        if common.randint(0, 1): grid[row][col] = bgcolor
    for col in range(width):
      if common.randint(0, 1): continue
      for row in range(height):
        if common.randint(0, 1): grid[row][col] = bgcolor
    for row in range(height):
      for col in range(width):
        if not common.randint(0, 9):
          grid[row][col] = bgcolor * common.randint(0, 1)
    subset = common.random_colors(common.randint(2, 5), exclude=[4, bgcolor])
    val = common.randint(3, 10)
    thresh = common.randint(9, val * val)
    for row in range(height):
      for col in range(width):
        if not common.randint(0, thresh):
          grid[row][col] = common.choice(subset)
    subset = [subset[0]] + subset
    while True:
      brows = [common.randint(1, height - 2) for _ in subset]
      bcols = [common.randint(1, width - 2) for _ in subset]
      if not common.overlaps(brows, bcols, [5] * len(subset), [5] * sum(subset)):
        break
    for color, brow, bcol in zip(subset, brows, bcols):
      grid[brow][bcol] = 4
      for dr, dc in [(0, 1), (0, -1), (1, 0), (-1, 0)]:
        grid[brow + dr][bcol + dc] = color
    colors = common.flatten(grid)
    answer = subset[0]

  grid, output = common.grid(width, height), common.grid(1, 1, answer)
  for i, color in enumerate(colors):
    grid[i // width][i % width] = color
  return {"input": grid, "output": output}


