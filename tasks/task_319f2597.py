"""Generator."""

import common


def generate(brow=None, bcol=None, colors=None, size=20):
  """Returns input and output grids according to the given parameters.

  Args:
    brow: The row of the black box.
    bcol: The column of the black box.
    colors: A list of colors to use.
    size: The size of the grid.
  """

  def draw():
    grid, output = common.grids(size, size)
    for i, color in enumerate(colors):
      output[i // size][i % size] = grid[i // size][i % size] = color
    common.rect(grid, 2, 2, brow, bcol, 0)
    common.rect(output, 2, 2, brow, bcol, 0)
    reds = 0
    for i in range(size - 1):
      for dr, dc in [(0, 0), (0, 1), (1, 0), (1, 1)]:
        if output[brow + dr][i + dc] != 2:
          output[brow + dr][i + dc] = 0
          reds += 1
        if output[i + dr][bcol + dc] != 2:
          output[i + dr][bcol + dc] = 0
          reds += 1
    if reds == 0: return None, None
    return grid, output

  if brow is None:
    while True:
      colors = [common.random_color() for _ in range(size * size)]
      brow, bcol = common.randint(4, size - 6), common.randint(4, size - 6)
      grid, _ = draw()
      if grid: break

  grid, output = draw()
  return {"input": grid, "output": output}


