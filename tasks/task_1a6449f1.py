"""Generator."""

import common


def generate(width=None, height=None, wide=None, tall=None, brow=None,
             bcol=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    width: The width of the grid.
    height: The height of the grid.
    wide: The width of the box.
    tall: The height of the box.
    brow: The row of the box.
    bcol: The column of the box.
    colors: The colors of the grid.
  """

  if width is None:
    width, height = common.randint(12, 30), common.randint(12, 30)
    num_boxes = 2 if width * height < 475 else 3
    grid = common.grid(width, height)
    for r in range(height):
      for c in range(width):
        if common.randint(0, 3): continue
        grid[r][c] = common.random_color()
    while True:
      wides = sorted(common.sample(list(range(5, 11)), num_boxes))
      talls = sorted(common.sample(list(range(5, 11)), num_boxes))
      brows = [common.randint(0, height - tall) for tall in talls]
      bcols = [common.randint(0, width - wide) for wide in wides]
      if not common.overlaps(brows, bcols, wides, talls, 1): break
    hues = common.random_colors(num_boxes)
    for wide, tall, brow, bcol, hue in zip(wides, talls, brows, bcols, hues):
      common.hollow_rect(grid, wide, tall, brow, bcol, hue)
    wide, tall = wides[-1] - 2, talls[-1] - 2
    brow, bcol = brows[-1] + 1, bcols[-1] + 1
    colors = common.flatten(grid)

  grid, output = common.grid(width, height), common.grid(wide, tall)
  for i, color in enumerate(colors):
    grid[i // width][i % width] = color
  for r in range(tall):
    for c in range(wide):
      output[r][c] = grid[brow + r][bcol + c]
  return {"input": grid, "output": output}


