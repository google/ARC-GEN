"""Generator."""

import common


def generate(colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    colors: A list of colors to use.
  """

  def draw():
    grid, output = common.grids(22, 22)
    prow, pcol = 0, 0
    for i, color in enumerate(colors):
      output[i // 22][i % 22] = grid[i // 22][i % 22] = color
      if color == 4: prow, pcol = max(prow, i // 22), max(pcol, i % 22)
    prow, pcol = prow - 1, pcol - 1
    coords = []
    for row in range(0, 22):
      for col in range(0, 22):
        match, bad = True, False
        for dr in [-1, 0, 1]:
          for dc in [-1, 0, 1]:
            nr, nc = row + dr, col + dc
            if nr < 0 or nr >= 22 or nc < 0 or nc >= 22: continue
            if grid[prow + dr][pcol + dc] == 4:
              if grid[nr][nc] not in [0, 4]: bad = True
            elif grid[prow + dr][pcol + dc] != grid[nr][nc]: match = False
        if not match: continue
        if bad: return None, None
        if row < 1 or row >= 21 or col < 1 or col >= 21: return None, None
        coords.append((row, col))
    rows, cols = zip(*coords)
    if common.overlaps(rows, cols, [7] * len(coords), [7] * len(coords)):
      return None, None
    for row, col in coords:
      for dr in [-1, 0, 1]:
        for dc in [-1, 0, 1]:
          output[row + dr][col + dc] = grid[prow + dr][pcol + dc]
    return grid, output

  if colors is None:
    pattern = [common.choice([1, 8]) for _ in range(9)]
    if common.randint(0, 1):
      pattern[0] = pattern[2] = pattern[6] = pattern[8] = 0
      if common.randint(0, 1): pattern[4] = 0
    else:
      pattern[1] = pattern[3] = pattern[5] = pattern[7] = 0
    pattern[common.choice([0, 1, 2, 3, 5, 6, 7, 8])] = 0  # Avoid middle.
    num_boxes = common.randint(2, 3)
    while True:
      rows = [common.randint(1, 20) for _ in range(num_boxes)]
      cols = [common.randint(1, 20) for _ in range(num_boxes)]
      if not common.overlaps(rows, cols, [7] * num_boxes, [7] * num_boxes):
        break
    while True:
      grid = common.grid(22, 22)
      for row in range(22):
        for col in range(22):
          grid[row][col] = 0 if common.randint(0, 4) else common.choice([1, 8])
      for i, (row, col) in enumerate(zip(rows, cols)):
        for dr in [-1, 0, 1]:
          for dc in [-1, 0, 1]:
            color = pattern[(dr + 1) * 3 + dc + 1]
            grid[row + dr][col + dc] = 4 if not i and not color else color
      colors = common.flatten(grid)
      grid, _ = draw()
      if grid: break

  grid, output = draw()
  return {"input": grid, "output": output}


