"""Generator."""

import common


def generate(prow=None, pcol=None, colors=None, noise=None, size=19):
  """Returns input and output grids according to the given parameters.

  Args:
    prow: The row of the plus.
    pcol: The column of the plus.
    colors: The colors of the main elements.
    noise: The background input noise.
    size: The size of the input grid.
  """

  if prow is None:
    prow, pcol = common.randint(6, 12), common.randint(6, 12)
    colors = common.sample(list(range(10)), 5)
    noise = [common.random_color() for _ in range(size * size)]

  grid, output = common.grid(size, size), common.grid(3, 3)
  for i, color in enumerate(noise):
    grid[i // size][i % size] = color
  for i in range(size):
    grid[prow][i] = grid[i][pcol] = colors[4]
  common.rect(grid, pcol - 2, prow - 2, 1, 1, colors[0])
  common.rect(grid, size - pcol - 3, prow - 2, 1, pcol + 2, colors[1])
  common.rect(grid, pcol - 2, size - prow - 3, prow + 2, 1, colors[2])
  common.rect(grid, size - pcol - 3, size - prow - 3, prow + 2, pcol + 2, colors[3])
  output[0][0] = colors[0]
  output[0][2] = colors[1]
  output[2][0] = colors[2]
  output[2][2] = colors[3]
  output[0][1] = output[1][0] = output[1][1] = output[1][2] = output[2][1] = colors[4]
  return {"input": grid, "output": output}


