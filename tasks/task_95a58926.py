"""Generator."""

import common


def generate(width=None, height=None, spacing=None, color=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    size: The size of the grid.
    rows: The rows of the boxes.
    cols: The columns of the boxes.
    thicks: The thicknesses of the boxes.
  """

  if width is None:
    width, height = common.randint(12, 30), common.randint(12, 30)
    spacing, color = common.randint(3, 5), common.random_color(exclude=[5])
    grid = common.grid(width, height)
    for r in range(height):
      for c in range(spacing, width, spacing + 1):
        grid[r][c] = 5
    for r in range(spacing, height, spacing + 1):
      for c in range(width):
        grid[r][c] = 5
    for r in range(height):
      for c in range(width):
        if common.randint(0, 9) == 0: grid[r][c] = color
    colors = common.flatten(grid)

  grid, output = common.grids(width, height)
  for i, c in enumerate(colors):
    grid[i // width][i % width] = c
  for r in range(height):
    for c in range(spacing, width, spacing + 1):
      output[r][c] = 5
  for r in range(spacing, height, spacing + 1):
    for c in range(width):
      output[r][c] = 5
  for r in range(spacing, height, spacing + 1):
    for c in range(spacing, width, spacing + 1):
      output[r][c] = color
  return {"input": grid, "output": output}


