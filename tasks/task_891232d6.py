"""Generator."""

import common


def generate(width=None, height=None, pcols=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    width: The width of the grid.
    height: The height of the grid.
    pcols: The columns of the pink dots.
    colors: The colors of the grid.
  """

  def draw():
    grid, output = common.grids(width, height)
    for i, color in enumerate(colors):
      output[i // width][i % width] = grid[i // width][i % width] = color
    for pcol in pcols:
      row, col = height - 1, pcol
      output[row][col] = grid[row][col] = 6
      if output[row - 1][col] != 0: return None, None
      row -= 1
      while row > 0:
        if output[row - 1][col] == 0:
          output[row][col] = 2
        elif output[row - 1][col] == 7:
          if col + 1 >= width: return None, None
          if output[row][col + 1] == 7: break
          if output[row][col + 1] != 0: return None, None
          if output[row][col] != 0: return None, None
          output[row][col] = 4
          output[row - 1][col] = 8
          while True:
            col += 1
            if col >= width: return None, None
            if output[row - 1][col] == 0: break
            if output[row - 1][col] != 7: return None, None
            if output[row][col] != 0: return None, None
            output[row][col] = 2
          if output[row][col] != 0: return None, None
          output[row][col] = 3
        else:
          return None, None
        row -= 1
      if output[row][col] != 0: return None, None
      output[row][col] = 6
      if col == pcol: return None, None
    return grid, output

  if width is None:
    width, height = common.randint(10, 30), common.randint(10, 30)
    num_lines = width * height // 100 + 1
    num_dots = common.randint(0, width + height)
    num_pink = common.randint(1, 3)
    while True:
      pcols = common.sample(list(range(width)), num_pink)
      if common.overlaps_1d(pcols, [4] * num_pink): continue
      colors = common.grid(width, height)
      for _ in range(num_lines):
        length = common.randint(3, 7)
        row = common.randint(0, height - 2)
        col = common.randint(0, width - length)
        for c in range(col, col + length):
          colors[row][c] = 7
      for _ in range(num_dots):
        row = common.randint(0, height - 1)
        col = common.randint(0, width - 1)
        colors[row][col] = 7
      colors = common.flatten(colors)
      grid, _ = draw()
      if grid: break

  grid, output = draw()
  return {"input": grid, "output": output}


