"""Generator."""

import common


def generate(minisize=None, megasize=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
    minisize: The size of the mini grid.
    megasize: The size of the mega grid.
    colors: The colors of the grid.
  """

  if minisize is None:
    while True:
      minisize = common.randint(3, 5)
      megasize = common.randint(3, 6)
      size = (minisize + 1) * megasize - 1
      if size <= 30: break
    grid = common.grid(size, size)
    bgcolor = common.random_color()
    fgcolor = common.random_color(exclude=[bgcolor])
    # Draw the gridlines.
    for r in range(size):
      for c in range(minisize, size, minisize + 1):
        grid[r][c] = bgcolor
    for r in range(minisize, size, minisize + 1):
      for c in range(size):
        grid[r][c] = bgcolor
    # Draw random colors everywhere.
    for row in range(megasize):
      for col in range(megasize):
        while True:
          hues = [common.randint(0, 3) for _ in range(minisize * minisize)]
          hues = [0 if hue else common.random_color() for hue in hues]
          if 0 in hues and len(set(hues)) > 2: break
        for r in range(minisize):
          for c in range(minisize):
            hue = hues[r * minisize + c]
            grid[row * (minisize + 1) + r][col * (minisize + 1) + c] = hue
    # Draw the foreground in just a single cell.
    row, col = common.randint(0, megasize - 1), common.randint(0, megasize - 1)
    while True:
      hues = [common.randint(0, 3) for _ in range(minisize * minisize)]
      hues = [0 if hue else fgcolor for hue in hues]
      if 0 in hues and hues.count(fgcolor) > minisize: break
    for r in range(minisize):
      for c in range(minisize):
        hue = hues[r * minisize + c]
        grid[row * (minisize + 1) + r][col * (minisize + 1) + c] = hue
    colors = common.flatten(grid)

  size = (minisize + 1) * megasize - 1
  grid, output = common.grid(size, size), common.grid(minisize, minisize)
  for i, color in enumerate(colors):
    grid[i // size][i % size] = color
  for row in range(megasize):
    for col in range(megasize):
      hues = []
      for r in range(minisize):
        for c in range(minisize):
          hues.append(grid[row * (minisize + 1) + r][col * (minisize + 1) + c])
      if len(set(hues)) > 2: continue
      for r in range(minisize):
        for c in range(minisize):
          hue = grid[row * (minisize + 1) + r][col * (minisize + 1) + c]
          output[r][c] = hue
  return {"input": grid, "output": output}


