"""Generator."""

import common


def generate(wides=None, talls=None, brows=None, bcols=None, colors=None):
  """Returns input and output grids according to the given parameters.

  Args:
  """

  if wides is None:
    hues = [1, 2, 3, 6, 8]
    grid = common.grid(30, 30, 4)
    for r in range(30):
      for c in range(30):
        grid[r][c] = common.choice(hues)
    num_boxes = common.randint(2, 4)
    while True:
      wides = [common.randint(4, 10) for _ in range(num_boxes)]
      talls = [common.randint(4, 10) for _ in range(num_boxes)]
      brows = [common.randint(2, 28 - tall) for tall in talls]
      bcols = [common.randint(2, 28 - wide) for wide in wides]
      if not common.overlaps(brows, bcols, wides, talls, 4): break
    shades = common.sample(list(range(len(hues))), num_boxes)
    for wide, tall, brow, bcol, shade in zip(wides, talls, brows, bcols, shades):
      common.rect(grid, wide, tall, brow, bcol, hues[shade])
      # Add some surrounding dust to be safe.
      grid[brow - 1][common.randint(bcol, bcol + wide - 1)] = hues[shade - 1]
      grid[brow + tall][common.randint(bcol, bcol + wide - 1)] = hues[shade - 1]
      grid[common.randint(brow, brow + tall - 1)][bcol - 1] = hues[shade - 1]
      grid[common.randint(brow, brow + tall - 1)][bcol + wide] = hues[shade - 1]
    colors = common.flatten(grid)

  grid, output = common.grids(30, 30)
  for i, color in enumerate(colors):
    output[i // 30][i % 30] = grid[i // 30][i % 30] = color
  for wide, tall, brow, bcol in zip(wides, talls, brows, bcols):
    common.rect(output, wide, tall, brow, bcol, 4)
  return {"input": grid, "output": output}


